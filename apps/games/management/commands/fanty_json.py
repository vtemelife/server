class Command(BaseCommand):
    def handle(self, *args, **options):

        path = os.path.join(settings.MEDIA_ROOT, "fanty")

        fanty = {}
        fanty_formats = ["mw", "mwm", "wmw", "mwmw", "party"]
        for fanty_format in fanty_formats:
            fanty_levels = [1, 2, 3, 4]
            fanty[fanty_format] = {}
            for fanty_level in fanty_levels:
                cards = []
                for dir_name, subdir_list, file_list in os.walk(path):
                    if "/%s/" % fanty_format not in dir_name:
                        continue
                    if "/%s уровень" % fanty_level not in dir_name:
                        continue
                    for fname in file_list:
                        cards.append(
                            {
                                "image": os.path.join(
                                    settings.MEDIA_URL, "fanty/%s/%s уровень/" % (fanty_format, fanty_level), fname
                                ),
                                "text": fname.replace(".png", ""),
                            }
                        )
                if len(cards) > 0:
                    fanty[fanty_format][fanty_level] = cards
                else:
                    fanty[fanty_format][fanty_level] = [
                        {"image": None, "text": i} for i in DEFAULT_FANTY[fanty_format][fanty_level]
                    ]

        with io.open("fanty.json", "w", encoding="utf8") as json_file:
            data = json.dumps(fanty, ensure_ascii=False)
            json_file.write(data)
